import turtle

# Create a canvas instance
myturtle = turtle.Turtle()

# goto a certain certain canvas
myturtle.penup()
myturtle.goto(50,75)

myturtle.pendown()
myturtle.forward(100)
myturtle.left(90)
myturtle.forward(200)
myturtle.left(90)
myturtle.forward(100)
myturtle.left(90)
myturtle.forward(200)


turtle.done()
